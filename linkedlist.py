class Node(object):

    def __init__(self, data):
        """Initialize this node with the given data."""
        self.data = data
        self.next = None

    def __repr__(self):
        """Return a string representation of this node."""
        return 'Node({!r})'.format(self.data)


class LinkedList(object):

    def __init__(self, items=None):
        """Initialize this linked list and append the given items, if any."""
        self.head = None  # First node
        self.tail = None  # Last node
        # Append given items
        if items is not None:
            for item in items:
                self.append(item)

    def __str__(self):
        """Return a formatted string representation of this linked list."""
        items = ['({!r})'.format(item) for item in self.items()]
        return '[{}]'.format(' -> '.join(items))

    def __repr__(self):
        """Return a string representation of this linked list."""
        return 'LinkedList({!r})'.format(self.items())

    def items(self):
        """Returns a list (dynamic array) of all items in this linked list.
        Best and worst case running time: O(n) for n items in the list (length)
        because we always need to loop through all n nodes to get each item."""
        items = []  # O(1) time to create empty list
        # Start at head node
        node = self.head  # O(1) time to assign new variable
        # Loop until node is None, which is one node too far past tail
        while node is not None:  # Always n iterations because no early return
            items.append(node.data)  # O(1) time (on average) to append to list
            # Skip to next node to advance forward in linked list
            node = node.next  # O(1) time to reassign variable
        # Now list contains items from all nodes
        return items  # O(1) time to return list

    def is_empty(self):
        """Return a boolean indicating whether this linked list is empty."""
        return self.head is None

    def length(self):
        """Returns the length of this linked list by traversing its nodes.
        Running time: O(n) Iterates through whole list"""
        counter = 0
        for i in self.items():
            counter += 1
        return counter

    def append(self, item):
        """Inserts the given item at the tail of this linked list.
        Running time: O(1) Tail is known"""
        new_node = Node(item)
        if self.head != None:
            if self.tail != None:
                self.tail.next = new_node
        else:
            self.head = new_node
        self.tail = new_node

    def prepend(self, item):
        """Insert the given item at the head of this linked list.
        Running time: O(1) Head is known"""
        new_node = Node(item)
        if self.head != None:
            new_node.next = self.head
        if self.tail == None:
            self.tail = new_node
        self.head = new_node

    def find(self, quality):
        """Return an item from this linked list satisfying the given quality.
        TODO: Best case running time: O(1) quality at beginning
        TODO: Worst case running time: O(n) quality at end"""
        if self.head != None:
            node = self.head
        else:
            return None
        while node != None:
            if quality(node.data) == True:
                return node.data
            node = node.next
        return None

        #return self.find(lambda data: data == quality)

    def delete(self, item):
        """Delete the given item from this linked list, or raise ValueError.
        TODO: Best case running time: O(1) Item at beginning
        TODO: Worst case running time: O(l) Item at end"""
        if self.head != None:
            node1 = self.head
        else:
            raise ValueError('Item not found: {}'.format(item))
        if self.head.next != None:
            if self.head.data == item:
                self.head = self.head.next
                if self.head.next == None:
                    self.tail = self.head
                return "item deleted at head"
            node2 = self.head.next
            while node2 != None:
                if node2.data == item:
                    if node2.next != None:
                        node1.next = node2.next
                    else:
                        node1.next = None
                        self.tail = node1
                    return "item deleted"
                node1 = node1.next
                node2 = node2.next
            raise ValueError('Item not found: {}'.format(item))
        else:
            if self.head.data == item:
                self.head = None
                if self.tail != None:
                    self.tail = None
                return "item and linked list deleted"
            raise ValueError('Item not found: {}'.format(item))


